# コラム ブロックのうしろに別のメソッドを続けて書く

names = ['田中', '鈴木', '佐藤']
san_names = names.map { |name| "#{name}さん" } #=> ["田中さん", "鈴木さん", "佐藤さん"]
san_names.join('と') #=> "田中さんと鈴木さんと佐藤さん"

